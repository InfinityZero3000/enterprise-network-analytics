export function getAlertDescription(description: string, lang: Lang): string {
  if (lang !== 'en') {
    return description;
  }

  if (description.includes('Dia chi buu dien ao co') || description.includes('Địa chỉ bưu điện ảo có')) {
    const match = description.match(/(\d+)/);
    const count = match ? Number(match[1]).toLocaleString() : 'many';
    return `Virtual mailbox address linked to ${count} companies.`;
  }

  if (description.includes('So huu vong tron') || description.includes('Sở hữu vòng tròn')) {
    const match = description.match(/(\d+)/);
    const hops = match ? match[1] : 'multiple';
    return `Circular ownership pattern detected with ${hops} nodes.`;
  }

  if (description.includes('Thuc the sieu ket noi voi') || description.includes('Thực thể siêu kết nối với')) {
    const match = description.match(/(\d+)/);
    const count = match ? Number(match[1]).toLocaleString() : 'many';
    return `Super-connected entity linked to ${count} companies.`;
  }

  return description;
}
